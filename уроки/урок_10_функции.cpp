bool isPrime(int n) {
    if (n < 2) return false;
    if (n == 2) return true;
    if (n % 2 == 0) return false;
    
    for (int i = 3; i * i <= n; i += 2) {
        if (n % i == 0) return false;
    }
    return true;
}

void exercise2() {
    int num;
    cout << "Введите число для проверки: ";
    cin >> num;
    
    if (isPrime(num)) {
        cout << num << " - простое число" << endl;
    } else {
        cout << num << " - не простое число" << endl;
    }
}

