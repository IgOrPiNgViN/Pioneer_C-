/*
===========================================
УРОК 5: ЦИКЛЫ
===========================================

ЦЕЛИ УРОКА:
- Изучить цикл for и его синтаксис
- Понять цикл while и его применение
- Освоить цикл do-while
- Научиться использовать вложенные циклы
- Понять операторы break и continue

ТЕОРЕТИЧЕСКАЯ ЧАСТЬ:

ЦИКЛЫ В C++:

1. ЦИКЛ FOR:
   for (инициализация; условие; обновление) {
       // тело цикла
   }

2. ЦИКЛ WHILE:
   while (условие) {
       // тело цикла
   }

3. ЦИКЛ DO-WHILE:
   do {
       // тело цикла
   } while (условие);

4. ВЛОЖЕННЫЕ ЦИКЛЫ:
   for (int i = 0; i < n; i++) {
       for (int j = 0; j < m; j++) {
           // код
       }
   }

УПРАВЛЕНИЕ ЦИКЛАМИ:
- break - выход из цикла
- continue - переход к следующей итерации

ОБЛАСТЬ ВИДИМОСТИ ПЕРЕМЕННЫХ:
- Переменные, объявленные в цикле for, видны только внутри цикла
- Переменные в while и do-while должны быть объявлены заранее

ПРИМЕРЫ КОДА:
*/

#include <iostream>
using namespace std;

int main() {
    // Цикл for
    cout << "=== ЦИКЛ FOR ===" << endl;
    for (int i = 1; i <= 5; i++) {
        cout << "Итерация " << i << endl;
    }
    
    // Цикл while
    cout << "\n=== ЦИКЛ WHILE ===" << endl;
    int count = 1;
    while (count <= 3) {
        cout << "Счетчик: " << count << endl;
        count++;
    }
    
    // Цикл do-while
    cout << "\n=== ЦИКЛ DO-WHILE ===" << endl;
    int num = 1;
    do {
        cout << "Число: " << num << endl;
        num++;
    } while (num <= 3);
    
    // Вложенные циклы
    cout << "\n=== ВЛОЖЕННЫЕ ЦИКЛЫ ===" << endl;
    for (int i = 1; i <= 3; i++) {
        for (int j = 1; j <= 3; j++) {
            cout << "(" << i << "," << j << ") ";
        }
        cout << endl;
    }
    
    // Оператор break
    cout << "\n=== ОПЕРАТОР BREAK ===" << endl;
    for (int i = 1; i <= 10; i++) {
        if (i == 5) {
            cout << "Выход на 5" << endl;
            break;
        }
        cout << i << " ";
    }
    cout << endl;
    
    // Оператор continue
    cout << "\n=== ОПЕРАТОР CONTINUE ===" << endl;
    for (int i = 1; i <= 10; i++) {
        if (i % 2 == 0) {
            continue; // пропускаем четные числа
        }
        cout << i << " ";
    }
    cout << endl;
    
    return 0;
}

