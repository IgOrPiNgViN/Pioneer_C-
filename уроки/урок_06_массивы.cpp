/*
Упражнение 4: Обращение массива
Создай программу для обращения массива (первый элемент становится последним и наоборот):
*/

void exercise4() {
    int arr[8] = {1, 2, 3, 4, 5, 6, 7, 8};
    
    cout << "Исходный массив: ";
    for (int i = 0; i < 8; i++) {
        cout << arr[i] << " ";
    }
    cout << endl;
    
    // Обращение массива
    int start = 0;
    int end = 8 - 1;
    
    while (start < end) {
        // Обмен элементов
        int temp = arr[start];
        arr[start] = arr[end];
        arr[end] = temp;
        
        start++;
        end--;
    }
    
    cout << "Обращенный массив: ";
    for (int i = 0; i < 8; i++) {
        cout << arr[i] << " ";
    }
    cout << endl;
}

/*
Упражнение 5: Сортировка пузырьком
Создай программу для сортировки массива методом пузырька:
*/

void exercise5() {
    int arr[6] = {64, 34, 25, 12, 22, 11};
    
    cout << "Исходный массив: ";
    for (int i = 0; i < 6; i++) {
        cout << arr[i] << " ";
    }
    cout << endl;
    
    // Сортировка пузырьком
    for (int i = 0; i < 6 - 1; i++) {
        for (int j = 0; j < 6 - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                // Обмен элементов
                int temp = arr[j];
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
            }
        }
    }
    
    cout << "Отсортированный массив: ";
    for (int i = 0; i < 6; i++) {
        cout << arr[i] << " ";
    }
    cout << endl;
}

