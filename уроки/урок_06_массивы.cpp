/*
Упражнение 2: Поиск максимума и минимума
Создай программу для поиска максимального и минимального элементов:
*/

void exercise2() {
    int n;
    cout << "Введите размер массива: ";
    cin >> n;
    
    int arr[100]; // Максимальный размер
    cout << "Введите " << n << " элементов:" << endl;
    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }
    
    int max = arr[0], min = arr[0];
    int maxIndex = 0, minIndex = 0;
    
    for (int i = 1; i < n; i++) {
        if (arr[i] > max) {
            max = arr[i];
            maxIndex = i;
        }
        if (arr[i] < min) {
            min = arr[i];
            minIndex = i;
        }
    }
    
    cout << "\n=== РЕЗУЛЬТАТЫ ===" << endl;
    cout << "Максимальный элемент: " << max << " на позиции " << maxIndex << endl;
    cout << "Минимальный элемент: " << min << " на позиции " << minIndex << endl;
}

