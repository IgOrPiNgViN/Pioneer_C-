/*
===========================================
УРОК 2: ПЕРЕМЕННЫЕ И ТИПЫ ДАННЫХ
===========================================

ЦЕЛИ УРОКА:
- Изучить основные типы данных в C++
- Научиться объявлять и инициализировать переменные
- Понять разницу между различными типами данных
- Изучить правила именования переменных
- Научиться работать с константами

ТЕОРЕТИЧЕСКАЯ ЧАСТЬ:

ТИПЫ ДАННЫХ В C++:

1. ЦЕЛЫЕ ЧИСЛА:
   - int - целые числа (обычно 32 бита)
   - short - короткие целые числа (16 бит)
   - long - длинные целые числа (32-64 бита)
   - long long - очень длинные целые числа (64 бита)
   - char - символы (8 бит)

2. ЧИСЛА С ПЛАВАЮЩЕЙ ТОЧКОЙ:
   - float - числа с плавающей точкой (32 бита)
   - double - двойной точности (64 бита)
   - long double - расширенной точности (80+ бит)

3. ЛОГИЧЕСКИЙ ТИП:
   - bool - логические значения (true/false)

4. БЕЗЗНАКОВЫЕ ТИПЫ:
   - unsigned int, unsigned char и т.д.

ОБЪЯВЛЕНИЕ ПЕРЕМЕННЫХ:
Синтаксис: тип_данных имя_переменной;

ИНИЦИАЛИЗАЦИЯ ПЕРЕМЕННЫХ:
Синтаксис: тип_данных имя_переменной = значение;

ПРАВИЛА ИМЕНОВАНИЯ:
- Имена могут содержать буквы, цифры и символ _
- Имя не может начинаться с цифры
- Нельзя использовать ключевые слова
- Регистр имеет значение (age и Age - разные переменные)

КОНСТАНТЫ:
- const - константы времени компиляции
- #define - макросы препроцессора

ПРИМЕРЫ КОДА:
*/

#include <iostream>
#include <string>
using namespace std;

int main() {
    // Объявление и инициализация переменных разных типов
    
    // Целые числа
    int age = 15;
    short year = 2024;
    long population = 8000000000L;
    long long bigNumber = 123456789012345LL;
    
    // Числа с плавающей точкой
    float height = 1.75f;
    double weight = 65.5;
    long double pi = 3.141592653589793238L;
    
    // Логический тип
    bool isStudent = true;
    bool isWorking = false;
    
    // Символы
    char grade = 'A';
    char symbol = '@';
    
    // Беззнаковые типы
    unsigned int positiveNumber = 100;
    unsigned char smallNumber = 255;
    
    // Константы
    const int MAX_STUDENTS = 30;
    const double PI = 3.14159;
    const string SCHOOL_NAME = "Дворец Пионеров";
    
    // Вывод значений переменных
    cout << "=== ПЕРЕМЕННЫЕ РАЗНЫХ ТИПОВ ===" << endl;
    cout << "Возраст: " << age << endl;
    cout << "Год: " << year << endl;
    cout << "Население мира: " << population << endl;
    cout << "Большое число: " << bigNumber << endl;
    
    cout << "\n=== ЧИСЛА С ПЛАВАЮЩЕЙ ТОЧКОЙ ===" << endl;
    cout << "Рост: " << height << " м" << endl;
    cout << "Вес: " << weight << " кг" << endl;
    cout << "Пи: " << pi << endl;
    
    cout << "\n=== ЛОГИЧЕСКИЕ ЗНАЧЕНИЯ ===" << endl;
    cout << "Студент: " << (isStudent ? "Да" : "Нет") << endl;
    cout << "Работает: " << (isWorking ? "Да" : "Нет") << endl;
    
    cout << "\n=== СИМВОЛЫ ===" << endl;
    cout << "Оценка: " << grade << endl;
    cout << "Символ: " << symbol << endl;
    
    cout << "\n=== БЕЗЗНАКОВЫЕ ТИПЫ ===" << endl;
    cout << "Положительное число: " << positiveNumber << endl;
    cout << "Маленькое число: " << (int)smallNumber << endl;
    
    cout << "\n=== КОНСТАНТЫ ===" << endl;
    cout << "Максимум студентов: " << MAX_STUDENTS << endl;
    cout << "Пи: " << PI << endl;
    cout << "Название школы: " << SCHOOL_NAME << endl;
    
    return 0;
}

