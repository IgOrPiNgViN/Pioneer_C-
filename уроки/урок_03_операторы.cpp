// Упражнение 1: Демонстрация арифметических операций
// Показывает все арифметические операции с двумя числами:
void exercise1() {
    double num1 = 15.0, num2 = 4.0;
    
    cout << "Числа: " << num1 << " и " << num2 << endl;
    cout << num1 << " + " << num2 << " = " << (num1 + num2) << endl;
    cout << num1 << " - " << num2 << " = " << (num1 - num2) << endl;
    cout << num1 << " * " << num2 << " = " << (num1 * num2) << endl;
    cout << num1 << " / " << num2 << " = " << (num1 / num2) << endl;
    
    // Целочисленное деление и остаток
    int a = 15, b = 4;
    cout << "\nЦелочисленное деление: " << a << " / " << b << " = " << (a / b) << endl;
    cout << "Остаток от деления: " << a << " % " << b << " = " << (a % b) << endl;
}

// Упражнение 2: Сравнение чисел
// Показывает результаты операторов сравнения:
void exercise2() {
    int a = 10, b = 20;
    
    cout << "a = " << a << ", b = " << b << endl;
    cout << "\n=== РЕЗУЛЬТАТЫ СРАВНЕНИЙ ===" << endl;
    cout << a << " == " << b << ": " << (a == b) << endl;  // 0 (false)
    cout << a << " != " << b << ": " << (a != b) << endl;  // 1 (true)
    cout << a << " < " << b << ": " << (a < b) << endl;    // 1 (true)
    cout << a << " > " << b << ": " << (a > b) << endl;    // 0 (false)
    cout << a << " <= " << b << ": " << (a <= b) << endl;  // 1 (true)
    cout << a << " >= " << b << ": " << (a >= b) << endl;  // 0 (false)
}

// Упражнение 3: Калькулятор процентов
// Демонстрация арифметических операций на примере процентов:
void exercise3() {
    double principal = 10000.0;  // основная сумма
    double rate = 5.0;           // процентная ставка
    double time = 3.0;           // время в годах
    
    double interest = (principal * rate * time) / 100;
    double total = principal + interest;
    
    cout << "=== РАСЧЕТ ПРОЦЕНТОВ ===" << endl;
    cout << "Основная сумма: " << principal << endl;
    cout << "Процентная ставка: " << rate << "%" << endl;
    cout << "Время: " << time << " лет" << endl;
    cout << "Проценты: " << interest << endl;
    cout << "Общая сумма: " << total << endl;
}

