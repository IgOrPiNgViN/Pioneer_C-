/*
===========================================
УРОК 3: ОПЕРАТОРЫ И ВЫРАЖЕНИЯ
===========================================

ЦЕЛИ УРОКА:
- Изучить арифметические операторы
- Понять операторы сравнения
- Освоить логические операторы
- Изучить приоритет операций
- Научиться использовать операторы присваивания

ТЕОРЕТИЧЕСКАЯ ЧАСТЬ:

АРИФМЕТИЧЕСКИЕ ОПЕРАТОРЫ:
- + (сложение)
- - (вычитание)
- * (умножение)
- / (деление)
- % (остаток от деления)
- ++ (инкремент)
- -- (декремент)

ОПЕРАТОРЫ СРАВНЕНИЯ:
- == (равно)
- != (не равно)
- < (меньше)
- > (больше)
- <= (меньше или равно)
- >= (больше или равно)

ЛОГИЧЕСКИЕ ОПЕРАТОРЫ:
- && (логическое И)
- || (логическое ИЛИ)
- ! (логическое НЕ)

ОПЕРАТОРЫ ПРИСВАИВАНИЯ:
- = (присваивание)
- += (сложение с присваиванием)
- -= (вычитание с присваиванием)
- *= (умножение с присваиванием)
- /= (деление с присваиванием)
- %= (остаток с присваиванием)

ПРИОРИТЕТ ОПЕРАЦИЙ:
1. () - скобки
2. ++, -- - инкремент, декремент
3. *, /, % - умножение, деление, остаток
4. +, - - сложение, вычитание
5. <, >, <=, >= - сравнение
6. ==, != - равенство
7. && - логическое И
8. || - логическое ИЛИ
9. = - присваивание

ПРИМЕРЫ КОДА:
*/

#include <iostream>
using namespace std;

int main() {
    // Арифметические операторы
    int a = 10, b = 3;
    
    cout << "=== АРИФМЕТИЧЕСКИЕ ОПЕРАТОРЫ ===" << endl;
    cout << "a = " << a << ", b = " << b << endl;
    cout << "a + b = " << (a + b) << endl;
    cout << "a - b = " << (a - b) << endl;
    cout << "a * b = " << (a * b) << endl;
    cout << "a / b = " << (a / b) << endl;
    cout << "a % b = " << (a % b) << endl;
    
    // Инкремент и декремент
    int x = 5;
    cout << "\n=== ИНКРЕМЕНТ И ДЕКРЕМЕНТ ===" << endl;
    cout << "x = " << x << endl;
    cout << "++x = " << (++x) << endl;  // Префиксный инкремент
    cout << "x++ = " << (x++) << endl;  // Постфиксный инкремент
    cout << "x = " << x << endl;
    cout << "--x = " << (--x) << endl;  // Префиксный декремент
    cout << "x-- = " << (x--) << endl;  // Постфиксный декремент
    cout << "x = " << x << endl;
    
    // Операторы сравнения
    int num1 = 10, num2 = 20;
    cout << "\n=== ОПЕРАТОРЫ СРАВНЕНИЯ ===" << endl;
    cout << "num1 = " << num1 << ", num2 = " << num2 << endl;
    cout << "num1 == num2: " << (num1 == num2) << endl;
    cout << "num1 != num2: " << (num1 != num2) << endl;
    cout << "num1 < num2: " << (num1 < num2) << endl;
    cout << "num1 > num2: " << (num1 > num2) << endl;
    cout << "num1 <= num2: " << (num1 <= num2) << endl;
    cout << "num1 >= num2: " << (num1 >= num2) << endl;
    
    // Логические операторы
    bool p = true, q = false;
    cout << "\n=== ЛОГИЧЕСКИЕ ОПЕРАТОРЫ ===" << endl;
    cout << "p = " << p << ", q = " << q << endl;
    cout << "p && q: " << (p && q) << endl;
    cout << "p || q: " << (p || q) << endl;
    cout << "!p: " << (!p) << endl;
    cout << "!q: " << (!q) << endl;
    
    // Операторы присваивания
    int value = 10;
    cout << "\n=== ОПЕРАТОРЫ ПРИСВАИВАНИЯ ===" << endl;
    cout << "Начальное значение: " << value << endl;
    value += 5;
    cout << "value += 5: " << value << endl;
    value -= 3;
    cout << "value -= 3: " << value << endl;
    value *= 2;
    cout << "value *= 2: " << value << endl;
    value /= 4;
    cout << "value /= 4: " << value << endl;
    value %= 3;
    cout << "value %= 3: " << value << endl;
    
    // Приоритет операций
    int result = 2 + 3 * 4;
    cout << "\n=== ПРИОРИТЕТ ОПЕРАЦИЙ ===" << endl;
    cout << "2 + 3 * 4 = " << result << endl;
    cout << "(2 + 3) * 4 = " << ((2 + 3) * 4) << endl;
    cout << "2 + (3 * 4) = " << (2 + (3 * 4)) << endl;
    
    return 0;
}

/*
ПРАКТИЧЕСКИЕ УПРАЖНЕНИЯ:

Упражнение 1: Простой калькулятор
Создай программу, которая выполняет основные арифметические операции:
*/

void exercise1() {
    double num1, num2;
    char operation;
    
    cout << "Введите первое число: ";
    cin >> num1;
    cout << "Введите операцию (+, -, *, /): ";
    cin >> operation;
    cout << "Введите второе число: ";
    cin >> num2;
    
    double result;
    switch (operation) {
        case '+':
            result = num1 + num2;
            break;
        case '-':
            result = num1 - num2;
            break;
        case '*':
            result = num1 * num2;
            break;
        case '/':
            if (num2 != 0) {
                result = num1 / num2;
            } else {
                cout << "Ошибка: деление на ноль!" << endl;
                return;
            }
            break;
        default:
            cout << "Неверная операция!" << endl;
            return;
    }
    
    cout << num1 << " " << operation << " " << num2 << " = " << result << endl;
}

