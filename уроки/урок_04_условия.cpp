/*
===========================================
УРОК 4: УСЛОВНЫЕ ОПЕРАТОРЫ
===========================================

ЦЕЛИ УРОКА:
- Изучить оператор if и его варианты
- Понять оператор switch
- Научиться использовать вложенные условия
- Освоить тернарный оператор
- Научиться обрабатывать различные случаи

ТЕОРЕТИЧЕСКАЯ ЧАСТЬ:

УСЛОВНЫЕ ОПЕРАТОРЫ:

1. ОПЕРАТОР IF:
   if (условие) {
       // код выполняется, если условие истинно
   }

2. ОПЕРАТОР IF-ELSE:
   if (условие) {
       // код выполняется, если условие истинно
   } else {
       // код выполняется, если условие ложно
   }

3. ОПЕРАТОР IF-ELSE IF-ELSE:
   if (условие1) {
       // код для условия1
   } else if (условие2) {
       // код для условия2
   } else {
       // код для всех остальных случаев
   }

4. ОПЕРАТОР SWITCH:
   switch (переменная) {
       case значение1:
           // код для значения1
           break;
       case значение2:
           // код для значения2
           break;
       default:
           // код по умолчанию
           break;
   }

5. ТЕРНАРНЫЙ ОПЕРАТОР:
   условие ? значение_если_истина : значение_если_ложь

ЛОГИЧЕСКИЕ ВЫРАЖЕНИЯ:
- && (И) - оба условия должны быть истинными
- || (ИЛИ) - хотя бы одно условие должно быть истинным
- ! (НЕ) - инвертирует результат

ПРИМЕРЫ КОДА:
*/

#include <iostream>
using namespace std;

int main() {
    // Простой if
    int age = 18;
    cout << "=== ПРОСТОЙ IF ===" << endl;
    if (age >= 18) {
        cout << "Вы совершеннолетний" << endl;
    }
    
    // If-else
    int temperature = 25;
    cout << "\n=== IF-ELSE ===" << endl;
    if (temperature > 30) {
        cout << "Жарко!" << endl;
    } else {
        cout << "Не жарко" << endl;
    }
    
    // If-else if-else
    int score = 85;
    cout << "\n=== IF-ELSE IF-ELSE ===" << endl;
    if (score >= 90) {
        cout << "Отлично!" << endl;
    } else if (score >= 80) {
        cout << "Хорошо!" << endl;
    } else if (score >= 70) {
        cout << "Удовлетворительно" << endl;
    } else {
        cout << "Неудовлетворительно" << endl;
    }
    
    // Switch
    char grade = 'B';
    cout << "\n=== SWITCH ===" << endl;
    switch (grade) {
        case 'A':
            cout << "Отлично!" << endl;
            break;
        case 'B':
            cout << "Хорошо!" << endl;
            break;
        case 'C':
            cout << "Удовлетворительно" << endl;
            break;
        case 'D':
            cout << "Плохо" << endl;
            break;
        default:
            cout << "Неизвестная оценка" << endl;
            break;
    }
    
    // Тернарный оператор
    int a = 10, b = 20;
    cout << "\n=== ТЕРНАРНЫЙ ОПЕРАТОР ===" << endl;
    int max = (a > b) ? a : b;
    cout << "Максимум из " << a << " и " << b << " = " << max << endl;
    
    // Логические операторы
    bool isStudent = true;
    bool hasScholarship = false;
    cout << "\n=== ЛОГИЧЕСКИЕ ОПЕРАТОРЫ ===" << endl;
    cout << "Студент: " << isStudent << endl;
    cout << "Стипендия: " << hasScholarship << endl;
    cout << "Студент И стипендия: " << (isStudent && hasScholarship) << endl;
    cout << "Студент ИЛИ стипендия: " << (isStudent || hasScholarship) << endl;
    cout << "НЕ студент: " << (!isStudent) << endl;
    
    return 0;
}

